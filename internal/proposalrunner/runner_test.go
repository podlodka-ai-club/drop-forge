type fakeCommandRunner struct {
	commands  []recordedCommand
	responses []fakeResponse
}

type recordedCommand struct {
	name  string
	args  []string
	dir   string
	stdin string
}

type fakeResponse struct {
	stdout           string
	stderr           string
	err              error
	writeLastMessage bool
	lastMessage      string
}

func (runner *fakeCommandRunner) Run(ctx context.Context, command commandrunner.Command) error {
	stdin := ""
	if command.Stdin != nil {
		content, err := io.ReadAll(command.Stdin)
		if err != nil {
			return err
		}
		stdin = string(content)
	}

	runner.commands = append(runner.commands, recordedCommand{
		name:  command.Name,
		args:  append([]string(nil), command.Args...),
		dir:   command.Dir,
		stdin: stdin,
	})

	index := len(runner.commands) - 1
	if index >= len(runner.responses) {
		return nil
	}

	response := runner.responses[index]
	if response.writeLastMessage {
		lastMessagePath, ok := findArgValue(command.Args, "--output-last-message")
		if !ok {
			return errors.New("missing --output-last-message argument")
		}
		if err := os.MkdirAll(filepath.Dir(lastMessagePath), 0700); err != nil {
			return err
		}
		if err := os.WriteFile(lastMessagePath, []byte(response.lastMessage), 0600); err != nil {
			return err
		}
	}
	if response.stdout != "" && command.Stdout != nil {
		if _, err := io.WriteString(command.Stdout, response.stdout); err != nil {
			return err
		}
	}
	if response.stderr != "" && command.Stderr != nil {
		if _, err := io.WriteString(command.Stderr, response.stderr); err != nil {
			return err
		}
	}

	return response.err
}

func findArgValue(args []string, flag string) (string, bool) {
	for index := 0; index < len(args)-1; index++ {
		if args[index] == flag {
			return args[index+1], true
		}
	}

	return "", false
}

func assertCommand(t *testing.T, got recordedCommand, name string, args []string, dir string) {
	t.Helper()

	if got.name != name {
		t.Fatalf("command name = %q, want %q", got.name, name)
	}
	if !reflect.DeepEqual(got.args, args) {
		t.Fatalf("command args = %#v, want %#v", got.args, args)
	}
	if got.dir != dir {
		t.Fatalf("command dir = %q, want %q", got.dir, dir)
	}
}

func writeFile(t *testing.T, path string, content string) {
	t.Helper()

	if err := os.MkdirAll(filepath.Dir(path), 0700); err != nil {
		t.Fatalf("create directory for %s: %v", path, err)
	}
	if err := os.WriteFile(path, []byte(content), 0600); err != nil {
		t.Fatalf("write %s: %v", path, err)
	}
}

type logEvent struct {
	Module  string `json:"module"`
	Type    string `json:"type"`
	Message string `json:"message"`
}

func decodeLogEvents(t *testing.T, output string) []logEvent {
	t.Helper()

	lines := strings.Split(strings.TrimSpace(output), "\n")
	events := make([]logEvent, 0, len(lines))
	for _, line := range lines {
		if strings.TrimSpace(line) == "" {
			continue
		}

		var event logEvent
		if err := json.Unmarshal([]byte(line), &event); err != nil {
			t.Fatalf("decode log event %q: %v", line, err)
		}
		events = append(events, event)
	}

	return events
}

func assertLogMessage(t *testing.T, events []logEvent, module string, message string) {
	t.Helper()

	assertLogEvent(t, events, module, "info", message)
}

func assertLogEvent(t *testing.T, events []logEvent, module string, logType string, message string) {
	t.Helper()

	for _, event := range events {
		if event.Module == module && event.Type == logType && strings.Contains(event.Message, message) {
			return
		}
	}

	t.Fatalf("missing log event module=%q type=%q message containing %q in %#v", module, logType, message, events)
}

