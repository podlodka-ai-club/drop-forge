func TestRunnerHappyPathCreatesPRAndCommentFromLastMessage(t *testing.T) {
	task := "Add project roadmap workflow"
	cfg := validConfig()
	tempDir := filepath.Join(t.TempDir(), "orchv3-run")
	cloneDir := filepath.Join(tempDir, "repo")
	fake := &fakeCommandRunner{
		responses: []fakeResponse{
			{},
			{stdout: "?? openspec/changes/add-project-roadmap-workflow/proposal.md\n"},
			{},
			{},
			{},
			{},
			{stdout: "https://github.com/example/project/pull/42\n"},
			{},
		},
	}
	fakeAgent := &fakeAgentExecutor{
		result: AgentExecutionResult{FinalMessage: "Final agent response for PR comment."},
	}
	var stdout bytes.Buffer
	var stderr bytes.Buffer
	removeCalled := false

	runner := &Runner{
		Config:  cfg,
		Command: fake,
		Agent:   fakeAgent,
		Stdout:  &stdout,
		Stderr:  &stderr,
		MkdirTemp: func(dir string, pattern string) (string, error) {
			return tempDir, nil
		},
		RemoveAll: func(path string) error {
			removeCalled = true
			return nil
		},
		Now: fixedTime,
	}

	prURL, err := runner.Run(context.Background(), ProposalInput{Title: task, AgentPrompt: task})
	if err != nil {
		t.Fatalf("Run() returned error: %v", err)
	}

	if prURL != "https://github.com/example/project/pull/42" {
		t.Fatalf("prURL = %q", prURL)
	}
	if removeCalled {
		t.Fatal("RemoveAll called with CleanupTemp=false")
	}

	assertCommand(t, fake.commands[0], "git", []string{"clone", cfg.RepositoryURL, cloneDir}, tempDir)

	branchName := "codex/proposal/20260422100203-add-project-roadmap-workflow"
	assertCommand(t, fake.commands[1], "git", []string{"status", "--short"}, cloneDir)
	assertCommand(t, fake.commands[2], "git", []string{"checkout", "-b", branchName}, cloneDir)
	assertCommand(t, fake.commands[3], "git", []string{"add", "-A"}, cloneDir)
	assertCommand(t, fake.commands[4], "git", []string{"commit", "-m", "OpenSpec proposal: Add project roadmap workflow"}, cloneDir)
	assertCommand(t, fake.commands[5], "git", []string{"push", "-u", "origin", branchName}, cloneDir)
	assertCommand(t, fake.commands[6], "gh", []string{
		"pr", "create",
		"--base", "main",
		"--head", branchName,
		"--title", "OpenSpec proposal: Add project roadmap workflow",
		"--body", "Generated by Drop Forge proposal runner.\n\nTask:\nAdd project roadmap workflow\n",
	}, cloneDir)
	assertCommand(t, fake.commands[7], "gh", []string{
		"pr", "comment", "https://github.com/example/project/pull/42",
		"--body", "Final agent response for PR comment.",
	}, cloneDir)

	stdoutEvents := decodeLogEvents(t, stdout.String())
	assertLogMessage(t, stdoutEvents, "git", "?? openspec/changes/add-project-roadmap-workflow/proposal.md")
	assertLogMessage(t, stdoutEvents, "github", "https://github.com/example/project/pull/42")
	assertLogMessage(t, stdoutEvents, "github", "created PR comment from final agent response")
	if strings.TrimSpace(stderr.String()) != "" {
		t.Fatalf("stderr = %q, want empty for fake agent happy path", stderr.String())
	}
}

